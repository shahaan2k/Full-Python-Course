# UNION AND INTERSECTION IN SETS 

s1 = {10, 30, 50, 70, 100}
s2 = {20, 40, 60, 80, 100}

print(s1.union(s2)) # UNION PRINTS ALL THE VALUES IN S1 AND S2
print(s1.intersection(s2)) # INTERSECTION PRINTS THE COMMON VALUES FROM S1 AND S2 

# SUBSET AND SUPERSET EXAMPLES

s1 = {10, 30, 50, 70, 100}
s2 = {10, 30} # IF ALL THE EMENTS OF S2 ARE ALSO IN S1 THEN S1 IS SUPERSET OF S2 AND S2 IS SUBSET OF S1

print(s1.issuperset(s2))
print(s2.issubset(s1))
